nums = [1, 23, 45, 67, 89]
# len()
print(len(nums))
# pop()
print(nums.pop(1))
# insert()
nums.insert(2,34)
print(nums)
# del()
del nums[-1]
print(nums)

# extend()
nums.extend([9,8,87,65,45])
print(nums)

#  min(), max()
print(min(nums))
print(max(nums))
# sum()
print(sum(nums))
# sort()
nums.sort()
print(nums)

